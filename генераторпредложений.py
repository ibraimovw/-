import random

subjects = [
    "Ильяс", "Азамат", "Адинур", "Абылай", "Криш", "Баха", "Кот", "Ботяра", "Программист",
    "Учёный", "Геймер", "Повар", "Астронавт", "Хакер",
    "Дизайнер", "Инженер", "Студент", "Доктор", "Музыкант",
    "Блогер", "Пилот", "Спортсмен", "Аналитик", "Разработчик",
    "Школьник", "Сосед", "Друг", "Брат", "Тренер",
    "Маркетолог", "Архитектор", "Физик", "Математик", "Писатель"
]

verbs = ["любит", "создаёт", "ломает", "исследует",
         "анализирует", "улучшает", "программирует",
         "собирает", "тестирует"]

objects = ["код", "пиццу", "роботов", "алгоритмы",
           "игры", "нейросети", "машины", "данные",
           "телефоны", "проекты"]

adverbs = ["быстро", "медленно", "хаотично",
           "неожиданно", "креативно", "странно"]

places = [
    "в космосе", "дома", "в лаборатории", "на улице", "в интернете",
    "в гараже", "в школе", "в университете", "в подвале",
    "на сервере", "в облаке данных", "на Марсе", "в виртуальной реальности",
    "в секретной базе", "в параллельной вселенной"
]
kolvo = ["в 2 раза", "в 10 раз", "на 300%", "в миллионы раз"]

reasons = [
    "из-за гравитации",
    "из-за багов реальности",
    "из-за секретных технологий",
    "потому что это логично",
    "потому что система так решила",
    "из-за сбоя в матрице",
    "потому что алгоритм так захотел",
    "из-за квантовой неопределённости",
    "из-за перегрузки нейросети",
    "из-за солнечных вспышек",
    "потому что никто не проверял",
    "из-за скрытого обновления системы",
    "из-за вируса в коде мира",
    "из-за изменения физики пространства"
]

used = {}

def pick(lst):
    available = [x for x in lst if used.get(x, 0) < 2]

    if not available:
        return random.choice(lst)

    w = random.choice(available)
    used[w] = used.get(w, 0) + 1
    return w

def sentence():
    return f"{pick(subjects)} {pick(adverbs)} {pick(verbs)} {pick(objects)} {pick(places)}."

def funny():
    return f"{pick(subjects)} {pick(adverbs)} танцует с {pick(objects)} {pick(places)}!"

def fact():
    return (
        f"Забавный факт: "
        f"{pick(subjects)} {pick(verbs)} {pick(objects)} "
        f"{pick(kolvo)} чаще {pick(places)}, "
        f"{pick(reasons)}."
    )

total = 0

while True:
    used = {} 

    print("\n==============================")
    print("  ГЕНЕРАТОР СЛУЧАЙНЫХ ПРЕДЛОЖЕНИЙ by Ibr Amir ")
    print("==============================\n")

    print("1 — обычные предложения")
    print("2 — смешные предложения")
    print("3 — забавные факты")

    mode = input("Режим: ")

    try:
        count = int(input("Сколько хочешь сгенерировать?: "))
        print("\nРезультат:\n")

        if mode == "1":
            for _ in range(count):
                print(sentence())

        elif mode == "2":
            for _ in range(count):
                print(funny())

        elif mode == "3":
            for _ in range(count):
                print(fact())

        else:
            print("Неверный режим")
            continue

        total += count
        print("\nВсего сгенерировано:", total)

    except ValueError:
        print("Введи число")

    again = input("\nСгенерировать ещё? (да/нет): ").strip().lower()
    if again != "да":
        print("Итого сгенерировано:", total)
        break